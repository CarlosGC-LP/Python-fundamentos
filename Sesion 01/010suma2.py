#Sumaremos dos numeros introducidos desde el teclado

#Entrada de datos
n1 = int(input('Escribe el primer dato a sumar: '))
#Se convierte a formato de numero entero
n2 = int(input('Escribe el segundo dato a suma: '))

#Procesamiento de datos
resultado = n1 + n2

#Salida de datos
print('\nEl resultado de la suma es: ', resultado)